Задача: нужна структура JSON, которая отображает подробную информацию про товар.
Верни только валидный JSON (один объект) без лишнего текста, комментариев и дополнений.

Тип товара: {{ $type }}

Цель: использовать структуру для поиска в Meilisearch — структура должна быть продумана для поиска.

Требования к JSON:
- Обязательный ключ "tags": массив строк (до 20), варианты написания типа товара на русском/английском/украинском (включая возможный транслит/разные формы).
- Остальные ключи — поля товара (строки/числа/булевы), допускаются вложенные объекты.
- Для числовых величин (память/диск/экран и т.п.) добавляй:
  - числовое значение (например size: 512)
  - человекочитаемое (например humanSize: "512GB")
- Используй стабильные/предсказуемые имена ключей в camelCase.
- Не включай произвольные длинные тексты. Делай поля пригодными для фильтрации/сортировки/поиска.

Пример (для ноутбука, только как идея полей):
{
  "tags": ["laptop", "ноутбук", "лептоп", "ноут", "notebook", "лэптоп"],
  "producer": "acer",
  "model": "aspire 5",
  "processor": {"producer": "intel", "model": "i5-1235u", "threads": 12, "cores": 10, "arch": "x86"},
  "ram": {"size": 16, "type": "ddr4", "humanSize": "16GB"},
  "disk": {"type": "ssd", "size": 512, "humanSize": "512GB"},
  "color": "black"
}



